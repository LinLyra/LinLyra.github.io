#!/usr/bin/env node
/**
 * Regenerates lib/portfolio-orbit.ts from overview list pages.
 * Source fields: slug, projectName, industries[]
 */

import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, "..")

function parseStringArray(inner) {
  const out = []
  const re = /"((?:[^"\\]|\\.)*)"/g
  let m
  while ((m = re.exec(inner))) out.push(m[1].replace(/\\"/g, '"'))
  return out
}

function parseOverviewFile(absPath, section, hrefPrefix) {
  const content = fs.readFileSync(absPath, "utf8")
  const items = []
  const blockRe =
    /\{\s*slug:\s*"([^"]+)"[\s\S]*?projectName:\s*"((?:[^"\\]|\\.)*)"[\s\S]*?industries:\s*\[([\s\S]*?)\]/g
  let m
  while ((m = blockRe.exec(content))) {
    const slug = m[1]
    const title = m[2].replace(/\\"/g, '"')
    const industries = parseStringArray(m[3])
    items.push({ slug, title, section, href: `${hrefPrefix}/${slug}`, industries })
  }
  return items
}

const business = parseOverviewFile(path.join(root, "app/business/page.tsx"), "business", "/business")
const data = parseOverviewFile(path.join(root, "app/data/page.tsx"), "data", "/data")
const product = parseOverviewFile(path.join(root, "app/product/page.tsx"), "product", "/product")

const orbitItems = [...business, ...data, ...product]

const lines = []
lines.push(`export type OrbitSection = "business" | "data" | "product"`)
lines.push(``)
lines.push(`export type OrbitItem = {`)
lines.push(`  slug: string`)
lines.push(`  title: string`)
lines.push(`  section: OrbitSection`)
lines.push(`  href: string`)
lines.push(`  industries: string[]`)
lines.push(`}`)
lines.push(``)
lines.push(`// AUTO-GENERATED by scripts/sync-portfolio-orbit.mjs`)
lines.push(`// Source: app/business|data|product/page.tsx (projectName + industries).`)
lines.push(`// Run: npm run sync:orbit`)
lines.push(``)
lines.push(`export const orbitItems: OrbitItem[] = [`)

for (const it of orbitItems) {
  lines.push(`  {`)
  lines.push(`    slug: ${JSON.stringify(it.slug)},`)
  lines.push(`    title: ${JSON.stringify(it.title)},`)
  lines.push(`    section: ${JSON.stringify(it.section)} as OrbitSection,`)
  lines.push(`    href: ${JSON.stringify(it.href)},`)
  lines.push(`    industries: ${JSON.stringify(it.industries)},`)
  lines.push(`  },`)
}

lines.push(`]`)
lines.push(``)

fs.writeFileSync(path.join(root, "lib/portfolio-orbit.ts"), lines.join("\n"))
console.log(`sync-portfolio-orbit: ${orbitItems.length} items → lib/portfolio-orbit.ts`)
